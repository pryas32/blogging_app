package com.codewithdurgesh.blog.payloads;

public class RoleDto {

	private int id;
	private String name;

	// Default constructor
	public RoleDto() {
	}

	// Getter for id
	public int getId() {
		return id;
	}

	// Setter for id
	public void setId(int id) {
		this.id = id;
	}

	// Getter for name
	public String getName() {
		return name;
	}

	// Setter for name
	public void setName(String name) {
		this.name = name;
	}
}
